public class Calculator {

    public int addition(int c1, int c2) {
        return c1 + c2;
    }

    public int addition(int c1, int c2, int c3) {
        return c1 + c2 + c3;
    }

    public int addition(int c1, int c2, int c3, int c4) {
        return c1 + c2 + c3 + c4;
    }


    public int multiplication(int c1, int c2) {
        return c1 * c2;
    }

    public int multiplication(int c1, int c2, int c3) {
        return c1 * c2 * c3;
    }

    public int multiplication(int c1, int c2, int c3, int c4) {
        return c1 * c2 * c3 * c4;
    }
}